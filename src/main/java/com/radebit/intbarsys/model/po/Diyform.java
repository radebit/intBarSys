package com.radebit.intbarsys.model.po;


public class Diyform {

  private Integer id;
  private String diyKey;
  private String diyValue;


  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }


  public String getDiyKey() {
    return diyKey;
  }

  public void setDiyKey(String diyKey) {
    this.diyKey = diyKey;
  }


  public String getDiyValue() {
    return diyValue;
  }

  public void setDiyValue(String diyValue) {
    this.diyValue = diyValue;
  }

}
